SELECT * FROM CUSTOMER_ORDER


WITH FIRST_VISIT AS(
SELECT CUSTOMER_ID, MIN(ORDER_DATE) AS FIRST_VISIT_DATE FROM CUSTOMER_ORDER
GROUP BY CUSTOMER_ID)
,VISIT_FLAG AS
(SELECT CO.*,FV.FIRST_VISIT_DATE 
, CASE WHEN CO.ORDER_DATE = FV.FIRST_VISIT_DATE THEN 1 ELSE 0 END AS FIRST_VISIT_FLG
, CASE WHEN CO.ORDER_DATE != FV.FIRST_VISIT_DATE THEN 1 ELSE 0 END AS REPEAT_VISIT_FLG
FROM CUSTOMER_ORDER CO
INNER JOIN FIRST_VISIT FV ON CO.CUSTOMER_ID=FV.CUSTOMER_ID)
SELECT ORDER_DATE, SUM(FIRST_VISIT_FLG) AS NO_OF_NEW_CUSTOMERS, SUM(REPEAT_VISIT_FLG) AS NO_OF_REPEAT_CUSTOMER
FROM VISIT_FLAG
GROUP BY ORDER_DATE

